// ── Locale-aware getters for fallback data ──────────────────────────────────

export function getLocalizedServices(locale: string): Service[] {
  if (locale === "ge") return services;
  const trans = serviceTranslations[locale];
  if (!trans) return services;
  return services.map((s) => {
    const t = trans[s.id];
    return t ? { ...s, name: t.name, description: t.description, shortDescription: t.shortDescription } : s;
  });
}

export function getLocalizedDoctors(locale: string, limit?: number): Doctor[] {
  const src = limit ? doctors.slice(0, limit) : doctors;
  if (locale === "ge") return src;
  const trans = doctorTranslations[locale];
  if (!trans) return src;
  return src.map((d) => {
    const t = trans[d.id];
    return t
      ? {
          ...d,
          name: t.name,
          specialty: t.specialty,
          biography: t.biography,
          qualifications: t.qualifications,
          specializations: t.specializations,
          languagesSpoken: t.languagesSpoken,
        }
      : d;
  });
}

export function getLocalizedCheckupPackages(locale: string): CheckupPackage[] {
  if (locale === "ge") return checkupPackages;
  const trans = checkupTranslations[locale];
  if (!trans) return checkupPackages;
  return checkupPackages.map((pkg) => {
    const t = trans[pkg.id];
    return t
      ? { ...pkg, name: t.name, description: t.description, includedServices: t.includedServices }
      : pkg;
  });
}

export function getLocalizedReviews(locale: string): Review[] {
  if (locale === "ge") return reviews;
  const trans = reviewTranslations[locale];
  if (!trans) return reviews;
  return reviews.map((r) => {
    const t = trans[r.id];
    return t ? { ...r, author: t.author, text: t.text } : r;
  });
}
